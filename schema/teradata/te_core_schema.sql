-- =============================================================================
-- schema/teradata/te_core_schema.sql — Teradata Vantage T&E Schema
-- =============================================================================
-- Placeholders replaced by adapter_teradata.sh at deploy time via sed:
--   {{DB_NAME}}                — target Teradata database name
--   {{APP_USER}}               — application user to create/grant
--   {{APP_PASSWORD}}           — application user password
--   {{PERM_SPACE_MB}}          — permanent space in MB for the database
--   {{SPOOL_SPACE_MB}}         — spool space in MB for the app user
--   {{TBL_ORGANISATIONS}}      — table name (default: organisations)
--   {{TBL_PERSONNEL}}          — table name (default: personnel)
--   {{TBL_TEST_PROGRAMS}}      — table name (default: test_programs)
--   {{TBL_TEMP_DOCUMENTS}}     — table name (default: temp_documents)
--   {{TBL_TEST_PHASES}}        — table name (default: test_phases)
--   {{TBL_REQUIREMENTS}}       — table name (default: requirements)
--   {{TBL_TEST_CASES}}         — table name (default: test_cases)
--   {{TBL_VCRM_ENTRIES}}       — table name (default: vcrm_entries)
--   {{TBL_TEST_EVENTS}}        — table name (default: test_events)
--   {{TBL_TEST_RESULTS}}       — table name (default: test_results)
--   {{TBL_DEFECT_REPORTS}}     — table name (default: defect_reports)
--   {{TBL_EVIDENCE_ARTIFACTS}} — table name (default: evidence_artifacts)
--
-- KEY TERADATA DIFFERENCES FROM POSTGRESQL:
--   - DATABASE = namespace (not SCHEMA); each table fully qualified as DB.table
--   - No native UUID — use CHAR(36) with application-generated UUIDs
--   - PRIMARY INDEX controls AMP distribution (choose high-cardinality column)
--   - SET TABLE enforces row uniqueness; MULTISET allows duplicates
--   - BYTEINT (0/1) replaces BOOLEAN
--   - TIMESTAMP(0) replaces TIMESTAMPTZ
--   - VARCHAR max = 64000 bytes in LATIN; use UNICODE for international text
--   - COLLECT STATISTICS replaces PostgreSQL ANALYZE / partial indexes
--   - No IF NOT EXISTS on CREATE TABLE — BTEQ error handling used instead
--   - No auto-increment — UUIDs generated by the application layer
-- =============================================================================


-- =============================================================================
-- SECTION 1: DATABASE AND USER SETUP
-- =============================================================================

-- Create application database (BTEQ: skip if already exists)
CREATE DATABASE {{DB_NAME}} AS
   PERMANENT = {{PERM_SPACE_MB}} * 1024 * 1024,
   SPOOL     = {{SPOOL_SPACE_MB}} * 1024 * 1024;

-- Create application user (schema-equivalent access)
CREATE USER {{APP_USER}} AS
   PASSWORD         = '{{APP_PASSWORD}}',
   PERMANENT        = 100 * 1024 * 1024,
   SPOOL            = {{SPOOL_SPACE_MB}} * 1024 * 1024,
   DEFAULT DATABASE = {{DB_NAME}};

-- Grant full access on the database to the application user
GRANT ALL ON {{DB_NAME}} TO {{APP_USER}} WITH GRANT OPTION;


